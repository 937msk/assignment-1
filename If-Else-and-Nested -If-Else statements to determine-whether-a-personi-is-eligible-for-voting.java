package assigm;

public class Ifelse {

	    public static void main(String[] args) {
	       
	        int[] ages = {16, 18, 25};
	        boolean[] hasValidID = {true, false, true};

	        for (int i = 0; i < ages.length; i++) {
	            int age = ages[i];
	            boolean id = hasValidID[i];

	            System.out.println("Testing the Case " + (i+1) + ": Age = " + age + ", Has Valid ID = " + id);

	            if (age >= 18) {
	            	
	                
	                if (id) {
	                    System.out.println("You are eligible to vote.");
	                    
	                } else {
	                    System.out.println("You are eligible by age, but missing valid identification.");
	                    
	                }
	            } else {
	                System.out.println("You are not eligible to vote.");
	                
	            }
	            System.out.println("-------------------------");
	       }
	   
	    }
	}
